let currentLanguage = 'fr';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('selectedLanguage', lang);

    // Update active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });

    // Update all translated elements
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            if (element.innerHTML.includes('<br>')) {
                element.innerHTML = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Update page title
    const titleMap = {
        'fr': 'Burger Michel EIRL - Couverture & Isolation',
        'en': 'Burger Michel EIRL - Roofing & Insulation',
        'de': 'Burger Michel EIRL - Bedachung & Dämmung'
    };
    document.title = titleMap[lang] || titleMap['fr'];
}

function initLanguageSelector() {
    // Load saved language or default to French
    const savedLang = localStorage.getItem('selectedLanguage') || 'fr';
    setLanguage(savedLang);

    // Add click listeners to language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.dataset.lang);
        });
    });
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', initLanguageSelector);